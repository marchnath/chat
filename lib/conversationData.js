// Helper function for local translation
export const translateLocally = (text, targetLanguage) => {
  const trimmedText = text.trim();

  // Determine which language mapping to use based on target language
  let currentLang;
  if (targetLanguage === "russian") {
    currentLang = "english";
  } else if (targetLanguage === "english") {
    // Could be from Italian or Russian
    // Try both and see which has the translation
    if (phraseTranslations.italian && phraseTranslations.italian[trimmedText]) {
      return phraseTranslations.italian[trimmedText];
    }
    if (phraseTranslations.russian && phraseTranslations.russian[trimmedText]) {
      return phraseTranslations.russian[trimmedText];
    }
    currentLang = "russian"; // fallback to Russian if no Italian match
  } else if (targetLanguage === "italian") {
    currentLang = "english";
  } else {
    currentLang = "english"; // default fallback
  }

  // Try exact match in phraseTranslations first - this is the most accurate
  if (
    phraseTranslations[currentLang] &&
    phraseTranslations[currentLang][trimmedText]
  ) {
    return phraseTranslations[currentLang][trimmedText];
  }

  // Try exact match in basic translations (case-insensitive for single words)
  const lowerText = trimmedText.toLowerCase();
  if (translations[lowerText]) {
    return translations[lowerText][targetLanguage] || trimmedText;
  }

  // Only try partial matches for longer phrases (more than 2 words)
  const wordCount = trimmedText.split(" ").length;
  if (wordCount > 2) {
    for (const [key, value] of Object.entries(
      phraseTranslations[currentLang] || {}
    )) {
      if (trimmedText.includes(key) || key.includes(trimmedText)) {
        return value;
      }
    }
  }

  // If no good translation found, indicate unavailability
  const unavailableText =
    targetLanguage === "russian"
      ? " (перевод недоступен)"
      : targetLanguage === "italian"
      ? " (traduzione non disponibile)"
      : " (translation unavailable)";
  return trimmedText + unavailableText;
};

